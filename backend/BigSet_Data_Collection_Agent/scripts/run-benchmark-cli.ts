#!/usr/bin/env tsx
/**
 * Run every prompts.json entry through the collection CLI pipeline with
 * benchmark-aligned settings and Tinyfish agent enabled.
 *
 * Usage (from backend/BigSet_Data_Collection_Agent):
 *   npm run collect:benchmark
 *   npm run collect:benchmark -- --dry-run
 *   npm run collect:benchmark -- --only saas-pricing-pages,ny-ai-startup-careers
 *   npm run collect:benchmark -- --repair
 */
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import { runPipeline } from "../src/orchestrator/pipeline.js";

interface BenchmarkPrompt {
  id: string;
  quality: string;
  persona: string;
  prompt: string;
  requiredColumns: string[];
  expectedStress: string;
}

const scriptDir = dirname(fileURLToPath(import.meta.url));
const agentRoot = join(scriptDir, "..");
const repoRoot = join(agentRoot, "../..");
const defaultPromptsPath = join(repoRoot, "benchmarks/dataset-agent/prompts.json");
const defaultRunsBase = join(agentRoot, "runs/benchmark-cli");

const args = process.argv.slice(2);
const dryRun = args.includes("--dry-run");
const enableRepair = args.includes("--repair");
const onlyArg = args.find((arg) => arg.startsWith("--only="))?.slice("--only=".length)
  ?? (args.includes("--only") ? args[args.indexOf("--only") + 1] : undefined);
const targetRowsArg = args.find((arg) => arg.startsWith("--target-rows="))?.slice(
  "--target-rows=".length,
);
const targetRows = targetRowsArg ? Number.parseInt(targetRowsArg, 10) : 8;
const promptsPath =
  args.find((arg) => arg.startsWith("--prompts="))?.slice("--prompts=".length)
  ?? defaultPromptsPath;
const runsBase =
  args.find((arg) => arg.startsWith("--out="))?.slice("--out=".length)
  ?? defaultRunsBase;

if (Number.isNaN(targetRows) || targetRows <= 0) {
  throw new Error("--target-rows must be a positive integer");
}

const onlyIds = onlyArg
  ? new Set(onlyArg.split(",").map((id) => id.trim()).filter(Boolean))
  : null;

const prompts = JSON.parse(await readFile(promptsPath, "utf8")) as BenchmarkPrompt[];
const selected = prompts.filter((entry) => !onlyIds || onlyIds.has(entry.id));

if (selected.length === 0) {
  throw new Error("No prompts matched the selection.");
}

await mkdir(runsBase, { recursive: true });

const commandsPath = join(repoRoot, "benchmarks/dataset-agent/cli-prompts.sh");
await writeFile(commandsPath, buildShellScript(selected, { targetRows, enableRepair }));

if (dryRun) {
  console.log(`Wrote ${commandsPath}`);
  console.log(`Would run ${selected.length} prompt(s) with agent enabled.`);
  for (const entry of selected) {
    console.log(`  - ${entry.id}`);
  }
  process.exit(0);
}

const manifest: Array<{
  promptId: string;
  quality: string;
  runId: string;
  outputRoot: string;
  recordCount: number;
  visualizationRecords: number;
}> = [];

for (const [index, entry] of selected.entries()) {
  const label = `${String(index + 1).padStart(2, "0")}-${entry.id}`;
  const outputDir = join(runsBase, label);

  console.log(`\n=== [${index + 1}/${selected.length}] ${entry.id} (${entry.quality}) ===`);
  console.log(`Prompt: ${entry.prompt.slice(0, 80)}${entry.prompt.length > 80 ? "…" : ""}`);
  console.log(`Agent: enabled | Repair: ${enableRepair ? "on" : "off"} | targetRows: ${targetRows}`);

  const result = await runPipeline({
    prompt: entry.prompt,
    targetRows,
    outputDir,
    enableRepair,
    enableTriage: true,
    enableTinyfishAgent: true,
    benchmark: {
      promptId: entry.id,
      promptQuality: entry.quality,
      persona: entry.persona,
      expectedStress: entry.expectedStress,
      requiredColumns: entry.requiredColumns,
    },
    onLog: (stage, message) => {
      console.error(`[${entry.id}:${stage}] ${message}`);
    },
  });

  manifest.push({
    promptId: entry.id,
    quality: entry.quality,
    runId: result.runId,
    outputRoot: result.paths.root,
    recordCount: result.recordCount,
    visualizationRecords: result.report.stats.visualization_records ?? 0,
  });

  console.log(
    `Done: run_id=${result.runId} records=${result.recordCount} ` +
      `visualization=${result.report.stats.visualization_records ?? 0}`,
  );
  console.log(`Artifacts: ${result.paths.root}`);
}

const manifestPath = join(runsBase, "manifest.json");
await writeFile(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);
console.log(`\nWrote manifest: ${manifestPath}`);
console.log(`Wrote shell reference: ${commandsPath}`);

function buildShellScript(
  entries: BenchmarkPrompt[],
  options: { targetRows: number; enableRepair: boolean },
): string {
  const lines = [
    "#!/usr/bin/env bash",
    "# Generated by: npm run collect:benchmark -- --dry-run",
    "# Run from repo root or adjust cd path. Tinyfish agent is ON (do not pass --no-agent).",
    "set -euo pipefail",
    "cd \"$(dirname \"$0\")/../../backend/BigSet_Data_Collection_Agent\"",
    "",
  ];

  for (const [index, entry] of entries.entries()) {
    const label = `${String(index + 1).padStart(2, "0")}-${entry.id}`;
    const repairFlag = options.enableRepair ? "" : "  --no-repair \\";
    lines.push(`# ${entry.id} (${entry.quality})`);
    lines.push("npm run collect -- run \\");
    lines.push(`  -p ${shellEscape(entry.prompt)} \\`);
    lines.push(`  -t ${options.targetRows} \\`);
    if (repairFlag) {
      lines.push(repairFlag);
    }
    lines.push(`  -o ${shellEscape(`runs/benchmark-cli/${label}`)} \\`);
    lines.push(
      `  --required-columns ${entry.requiredColumns.join(",")} \\`,
    );
    lines.push(`  --expected-stress ${shellEscape(entry.expectedStress)}`);
    lines.push("");
  }

  return `${lines.join("\n")}`;
}

function shellEscape(value: string): string {
  return `'${value.replace(/'/g, `'\\''`)}'`;
}
